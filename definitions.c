#include <stdio.h>



int Add(int a, int b)
 {
  int val;
  val=a+b;
  return val; 
 }

int Subtract(int a, int b)
 {
  int val;
  val=a-b;
  return val; 
 }

int Multiply(int a, int b)
 {
  int val;
  val=a*b;
  return val; 
 }

int Divide(int a, int b)
 {
  int val;
  val=a/b;
  return val; 
 }